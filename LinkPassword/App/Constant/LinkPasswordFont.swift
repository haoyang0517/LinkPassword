import Foundation
import UIKit

extension LinkPassword.Fonts {
    static let sfProTextRegular = "SFProText-Regular"
    
    static func sfProTextRegular(size: CGFloat) -> UIFont {
        return UIFont(name: sfProTextRegular, size: size)!
    }
    
}

